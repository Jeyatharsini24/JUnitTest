package com.jeya.nongui;

public class ComplexData {
	private String s;
	private int n;

	public ComplexData(String s, int n) {
		this.n = n;
		this.s = s;
	}

	public String getS() {
		return s;
	}

	public void setS(String s) {
		this.s = s;
	}

	public int getN() {
		return n;
	}

	public void setN(int n) {
		this.n = n;
	}

}
